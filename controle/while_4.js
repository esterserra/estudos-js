// quantidade determinada de repetições || se tornar falso em 3 passos

let controle = 1; // 1. variavel pra ajudar a controlar o laço

while (controle <= 10) { // 2. expressão verdadeira
    console.log(controle); 
    controle++; // 3. lógica que modifica a variável de controle. vai parar em 11 porque 11 não é menor que 10. repetiu 10x
}

console.log('Fim!');

// declaração, expressão e incremento = quantidade determinada de repetições